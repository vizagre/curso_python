lista = ["Pera", "Maçã", "Banana", "Uva"]

print(lista)

print(lista[0]) # Acessando o primeiro elemento da lista

print(lista[0:3]) # Acessando um intervalo de elementos da lista

lista.append("Laranja") # Adicionando um elemento ao final da lista
print(lista) # Imprimindo a lista após adicionar um elemento

lista.insert(1, "Abacaxi") # Adicionando um elemento em uma posição específica da lista
print(lista) # Imprimindo a lista após adicionar um elemento em uma posição específica

print(lista[2:]) # Imprimindo a lista a partir do índice 2 até o final    
print(lista[1:4]) # Imprimindo a lista do índice 1 até o índice 3 (o índice 4 não é incluído)
